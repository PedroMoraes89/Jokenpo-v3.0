import UIKit

extension UIColor {
    
    static func lightBlue () -> UIColor {
        UIColor(red: 91.0/255.0, green: 93.0/255.0, blue: 150.0/255.0, alpha: 1.0)
    }
    
    static func darkBlue () -> UIColor {
        UIColor(red: 31.0/255, green: 27.0/255, blue: 85.0/255, alpha: 1.0)
    }
    
    static func white () -> UIColor {
        UIColor(red: 255.0/255.0, green: 255.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
}
